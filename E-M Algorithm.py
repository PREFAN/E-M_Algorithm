# Given data and initial parameters
X = np.array([50, 55, 60, 65, 70, 85, 90, 95], dtype=float)
mu1, mu2 = 55.0, 85.0
var1, var2 = 31.25, 87.5  # variances computed from initial parameters
pi1, pi2 = 0.5, 0.5

def gaussian_pdf(x, mu, var):
    # Multivariate factor reduces to 1D Gaussian PDF
    coef = 1.0 / np.sqrt(2 * np.pi * var)
    exponent = -0.5 * ((x - mu)**2) / var
    return coef * np.exp(exponent)

# Compute likelihoods and responsibilities
